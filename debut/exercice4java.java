public class exercice4java {

/*  EXERCICE 4 : Faire le compte à rebour à partir de 10, affiche successivment décompte une fois à 0, afficher bonne année ! */
    public static void main(String[] args) throws Exception
    {

int décompte = 10;

while (décompte<=10) 
{

    System.out.print("\n");
    décompte=décompte-1;
   
System.out.print(décompte);
System.out.print("bonne année !!! :)");

}



    }
}
